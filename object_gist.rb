class Television
  def initialize(brand, size, style)
    @brand = brand
    @size = size
    @style = style
  end
end

class TelevisionShow
  def initialize(name, main_star, network)
    @name = name
    @main_star = main_star
    @network = network
  end
end

class TelevisionChannel
  def initialize(number, abbreviation)
    @number = number
    @abbreviation = abbreviation
  end
end




